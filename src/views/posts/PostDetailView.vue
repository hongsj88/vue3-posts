<template>
  <h2>게시글 상세</h2>
  <AppLoading v-if="loading" />
  <AppError v-else-if="error" :message="error.message" />

  <div v-else>
    <hr class="my-4" />

    <h2>{{ post.title }}</h2>
    <p>{{ post.content }}</p>
    <p class="text-muted">
      {{ $dayjs(post.createdAt).format('YYYY.MM.DD HH:mm:ss') }}
    </p>
    <hr class="my-4" />
    <AppError v-if="removeError" :message="removeError.message" />
    <div class="row g-2">
      <div class="col-auto">
        <button class="btn btn-outline-dark">이전글</button>
      </div>
      <div class="col-auto">
        <button class="btn btn-outline-dark">다음글</button>
      </div>
      <div class="col-auto me-auto"></div>
      <div class="col-auto">
        <button class="btn btn-outline-dark" @click="goListPage">목록</button>
      </div>
      <div class="col-auto">
        <button class="btn btn-outline-primary" @click="goEditPage">
          수정
        </button>
      </div>
      <div class="col-auto">
        <button
          class="btn btn-outline-danger"
          @click="remove"
          :disabled="removeLoading"
        >
          <template v-if="removeLoading">
            <span
              class="spinner-grow spinner-grow-sm"
              role="status"
              aria-hidden="true"
            ></span>
            <span class="visually-hidden">Loading...</span>
          </template>
          <template v-else> 삭제 </template>
        </button>
      </div>
    </div>
  </div>
</template>

<script setup>
import { useRouter } from 'vue-router';
import { getPostById, deletePost } from '@/api/posts';
import { ref } from 'vue';
const error = ref(null);
const loading = ref(false);

const props = defineProps({
  id: [String, Number],
});

// console.log('----props.id', props.id);
/**
 * ref
 * 장) 객체 할당 가능
 * 단) form.value.title, form.value.content
 * 장) 일관성 유지
 * reactive
 * 장) form.title, form.content
 * 단) 객체할당 불가능
 */

const router = useRouter();
const post = ref({});
const fetchPost = async () => {
  try {
    loading.value = true;
    console.log('detail id', props.id, typeof props.id);
    // console.log('type of', typeof props.id);
    const { data } = await getPostById(props.id);
    console.log('fetchbyid data::', data);
    // console.log('-------1 props.id', typeof props.id);
    // console.log('-------2 props.id', props.id);
    // console.log('data', data);
    setPost(data);
    // post.value = { ...data };
  } catch (err) {
    error.value = err;
    console.log(error);
  } finally {
    loading.value = false;
  }
};
const setPost = ({ title, content, createdAt }) => {
  post.value.title = title;
  post.value.content = content;
  post.value.createdAt = createdAt;
};
fetchPost();

const removeError = ref(null);
const removeLoading = ref(false);

const remove = async () => {
  try {
    if (confirm('삭제하시겠습니까?') === false) {
      return;
    }
    removeLoading.value = true;
    await deletePost(props.id);
    router.push({ name: 'PostList' });
  } catch (err) {
    console.log(err);
    error.value = err;
  } finally {
    removeLoading.value = false;
  }
};
const goListPage = () => router.push({ name: 'PostList' });
const goEditPage = () =>
  router.push({
    name: 'PostEdit',
    params: { id: props.id },
  });
</script>

